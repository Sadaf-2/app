# Tuple - ordered, immutable
person = ("NAME", 25, "Pakistan")
print(person[0])  # NAME

# Set - unordered, unique values
colors = {"red", "blue", "red", "green"}
print(colors)  # {'red', 'green', 'blue'}

# Add/remove in set
colors.add("yellow")
colors.discard("blue")
print(colors)
